/**
 * op_add - add to int
 * @a: 1st int
 * @b:
 * 2nd int
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub to int
 * @a: 1st int
 * @b:
 * 2nd int
 *
 * Return: sub of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul to int
 * @a: 1st int
 * @b:
 * 2nd int
 *
 * Return: mul of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div to int
 * @a: 1st int
 * @b:
 * 2nd int
 *
 * Return: div of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod of int
 * @a: 1st int
 * @b:
 * 2nd int
 *
 * Return: mod of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
